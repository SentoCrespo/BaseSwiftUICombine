import Foundation

public struct Typography {

    public enum fontSize: CGFloat {
        case extraSmall = 8
        case small = 12
        case medium = 17
        case large = 28
        case extraLarge = 34
    }
    
    public enum fontFamily: String {
        case helveticaNeue = "HelveticaNeue"
        case georgia = "Georgia"
    }
    
}
